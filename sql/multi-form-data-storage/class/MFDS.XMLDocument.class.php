<?php

/******************************************************************************/
/******************************************************************************/

class MFDSXMLDocument
{
    /**************************************************************************/

    private $document;
    
	/**************************************************************************/

	function __construct()
	{
        $this->document=null;
	}
    
    /**************************************************************************/

    public function getDocument()
    {
        $this->document='<?xml version="1.0" encoding="UTF-8"?>'.$this->document;
        return($this->document);
    }
    
    /**************************************************************************/
    
    public function openElement($name)
    {
        $this->document.='<'.$name.'>';
    }
    
    /**************************************************************************/
    
    public function closeElement($name)
    {
        $this->document.='</'.$name.'>';
    }
    
    /**************************************************************************/
    
    public function createElement($name,$content)
    {
        $this->document.=$this->openElement($name).'<![CDATA['.$content.']]>'.$this->closeElement($name);
    }
    
    /**************************************************************************/
}

